public class leftshift {
    public static void main(String[] args) {
        int a=5;
        int b=20;
        int c= -20;
        int result = a<<2;
        int result1=b>>2;
        int result2=c>>>2;
        System.out.println("a<<2 = "+result);
        System.out.println("b>>2="+result1);
        System.out.println("c>>>2 =" +result2);
    }
}
